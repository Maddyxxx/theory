	fmt.Printf("b %d\n", b)
    c := append(a, 5) // создание слайса с с элементами слайса а и добавление к с элемента 5 [1 2 3 7 5]

	fmt.Printf("a %d\n", a)
    fmt.Println(b) // [1 2 3 7 4]
