// Вариант 1

func f1() {
    a := []int{1,2} // создание слайса [1 2]
	fmt.Printf("a %d\n", a)
    a = append(a, 3) // добавление элемента 3 к слайсу а [1 2 3]
	fmt.Printf("a %d\n", a)
    b := append(a, 4) // создание слайса b с элементами слайса а и добавление к b элемента 4 [1 2 3 4]
	fmt.Printf("a %d\n", a)
	fmt.Printf("b %d\n", b)
    c := append(a, 5) // создание слайса c с элементами слайса а и добавление к c элемента 5 [1 2 3 5]
	fmt.Printf("a %d\n", a)

    fmt.Println(b) // тк на строке 15 на 4 позицию слайса а элемент 4 заменился на 5, и слайс b ссылается на элементы слайса а [1 2 3 5]
    fmt.Println(c, "\n\n") // [1 2 3 5]
}

